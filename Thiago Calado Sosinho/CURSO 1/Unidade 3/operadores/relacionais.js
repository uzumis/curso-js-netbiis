//Operadores relacionais

//Igual a (==)
let igual = 5 == 5;
console.log ('Igualdade: '+ igual); //true


//Identico a (===)
let identico = 5 ==='5';
console.log ("Igualdade restrita: "+ identico);//Flase


//Diferente de (!=)
let diferente = 5!= '5';
console.log("DiferenÃ§a: "+ diferente); //false

//Maior que (>)
let maiorQue = 5 > 3;
console.log("Maior que: "+ maiorQue);

//Menor que que (>)
let menorQue = 5 < 3;
console.log("Menor que: "+ menorQue);

//Maior ou igual que (>=)
let maiorIgual = 5 >= 3;
console.log("Maior ou igual que: "+ maiorIgual);

//Menor ou igual que (:<=)
let menorIgual = 5 <= 3;
console.log("Menor ou igual que: "+ menorIgual);