let arr = [1,2,3,4,5];

//push () adiciona um ou mais elementos no final do array
let testePush = arr.push(6)
console.log(testePush);
console.log(arr);

//unshift() adiciona um ou mais elementos no incinio do array
arr.unshift(0);
console.log(arr);

//pop() remove o ultimo elemento do array
arr.pop();
console.log(arr);

//shift() remove o primeiro elemento do array
arr.shift();
console.log(arr);